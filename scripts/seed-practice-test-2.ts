async function seed() {
  await connectDB()
  console.log('Connected to MongoDB.')

  const existingTest = await PracticeTest.findOne({
    examCode: '190',
    testNumber: 2,
    isDiagnostic: false,
  })

  if (existingTest) {
    if (process.env.CONFIRM_RESEED !== 'true') {
      console.log('\n🔴 Practice Test 2 already exists in MongoDB.')
      console.log('   To delete the existing test and its questions and reseed, run:')
      console.log('   CONFIRM_RESEED=true npx tsx scripts/seed-practice-test-2.ts')
      process.exit(0)
    }

    console.log('⚠️  CONFIRM_RESEED=true — deleting existing Practice Test 2 and its questions...')
    await Question.deleteMany({ _id: { $in: existingTest.questionIds } })
    await PracticeTest.deleteOne({ _id: existingTest._id })
    console.log('Deleted.')
  }

  const balanced = balanceDistribution(questionData)

  // Verify distribution
  const dist: Record<string, number> = { A: 0, B: 0, C: 0, D: 0 }
  for (const q of balanced) dist[q.correctAnswer]++
  console.log('Answer distribution:', dist)

  console.log(`Inserting ${balanced.length} questions...`)
  const inserted = await Question.insertMany(balanced)
  console.log(`Inserted ${inserted.length} questions.`)

  const questionIds = inserted.map((q: any) => q._id)

  const subareaI = inserted.filter((q: any) => q.subarea === 'I').length
  const subareaII = inserted.filter((q: any) => q.subarea === 'II').length
  const subareaIII = inserted.filter((q: any) => q.subarea === 'III').length
  console.log(`Subarea distribution: I=${subareaI}, II=${subareaII}, III=${subareaIII}`)

  const practiceTest = await PracticeTest.create({
    examCode: '190',
    testNumber: 2,
    name: 'Practice Test 2',
    isDiagnostic: false,
    questionIds,
    timeLimitMinutes: 240,
    subareaDistribution: [
      { subarea: 'I', count: subareaI },
      { subarea: 'II', count: subareaII },
      { subarea: 'III', count: subareaIII },
    ],
    isPublished: true,
  })

  console.log(`\n✅ Practice Test 2 created: ${practiceTest._id}`)
  process.exit(0)
}

