// ─── BALANCE DISTRIBUTION HELPER ──────────────────────────────────────────────
type Label = 'A' | 'B' | 'C' | 'D'

function balanceDistribution(questions: typeof questionData) {
  const targets: Record<Label, number> = { A: 25, B: 25, C: 25, D: 25 }
  const counts: Record<Label, number> = { A: 0, B: 0, C: 0, D: 0 }
  const labels: Label[] = ['A', 'B', 'C', 'D']

  return questions.map((q) => {
    const current = q.correctAnswer as Label
    const available = labels.filter((l) => counts[l] < targets[l])
    const target: Label = available.includes(current) ? current : available[0]
    counts[target]++
    if (target === current) return q
    const newOptions = q.options.map((o) => {
      if (o.label === current) return { ...o, label: target }
      if (o.label === target) return { ...o, label: current }
      return o
    })
    newOptions.sort((a, b) => a.label.localeCompare(b.label))
    return { ...q, options: newOptions, correctAnswer: target }
  })
}

